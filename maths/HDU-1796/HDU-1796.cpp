ll a[20]; ll n,  m,  cnt;
ll gcd(ll a, ll b){
    return b ? gcd(b, a % b) : a;
}

ll lcm(ll a, ll b) {
    return a / gcd(a, b) * b;
}

ll contribution(ll s) {
    ll sign=-1;
    ll l=1;
    For(i,cnt) {
        int j=(1<<i);
        if (j & s) {
            sign = -sign;
            l = lcm(l, a[i]);
        }
    }
    return (n/l)*sign;
}


